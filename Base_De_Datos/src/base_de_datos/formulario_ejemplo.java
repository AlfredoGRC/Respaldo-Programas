    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     
         //puede ser alanacenado en una variable entera                                         //
         int p = JOptionPane.showConfirmDialog(null, "¿Quieres eliminar? ","Eliminando Registro ", JOptionPane.OK_OPTION);

        if (p==0){
            int fila = tab.getSelectedRow();//selecciona lo que tine el DataGrid
            String ID = tab.getValueAt(fila, 0).toString();//fila el 0 del arreglo de la info de la db

            try{
                PreparedStatement pst = cn.prepareStatement("DELETE FROM informacion WHERE nombre='"+ID+"'");//ID es lo que esta seleccionado en la tabla
                pst.executeUpdate();//consultar y acltualiza entra el DELATE 
                mostrar("");//para poder eliminar el registro en la interfaz

            }catch (Exception e ){
                JOptionPane.showMessageDialog(null,"algo salio mal al eliminar el registro. " + e);

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        String nom, papa, mama, ege;
        String SQL = "informacion";
        
        nom = nomb.getText();
        papa = a_paterno.getText();
        mama = a_materno.getText();
        ege = edad.getText();
        
        SQL = "INSERT INTO informacion (nombre, a_paterno, a_materno, edad) VALUES (?,?,?,?)";//los signos de interrogacion estan en espera de la cadena
                                                                                              //que tiene la caja de texto   
        try{
            PreparedStatement pst =  cn.prepareStatement(SQL);//prepara las sentencias SQL pst = nombre de variable SQL es la variable de arriba
            pst.setString(1,nom);  // nombre de la variable donde gurdamos el TextBox
            pst.setString(2,papa); // nombre de la variable donde gurdamos el TextBox
            pst.setString(3,mama );// nombre de la variable donde gurdamos el TextBox
            pst.setString(4, ege );// nombre de la variable donde gurdamos el TextBox
  
            int n = pst.executeUpdate();
                            //actualizar --- consulta//
            mostrar(""); 
 
            if (n>=0){//array 
                JOptionPane.showMessageDialog(null,"Datos guardados ");  
                bloquear(); 
                limpiar();
            }
            
        }
        catch(SQLException ex){
            Logger.getLogger(formulario_ejemplo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

