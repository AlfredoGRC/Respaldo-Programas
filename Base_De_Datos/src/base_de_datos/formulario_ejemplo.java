    void mostrar(String valor){
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("nombre");
        modelo.addColumn("a_paterno");
        modelo.addColumn("a_materno");
        modelo.addColumn("edad");
        
        String SQL = "SELECT * FROM informacion";
        String SQL1 = "SELECT * FROM informacion WHERE nombre = ' "+valor + " '";
        
        String res = (valor.equals("")) ? SQL : SQL1;
        
        String [] datos = new String[4];
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(res);
            while(rs.next()){
                
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
            
              
                
                modelo.addRow(datos);
            }
            tab.setModel(modelo);
            
        }
        catch(SQLException e){
            Logger.getLogger(formulario_ejemplo.class.getName()).log(Level.SEVERE, null, e);
        }
       
    }
