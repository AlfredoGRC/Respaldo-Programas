    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        //
        int p = JOptionPane.showConfirmDialog(null,"Quieres eliminar el registro?","Eliminando registro",JOptionPane.OK_OPTION);
        
        if(p == 0){//por el arreglo debe de empezar rn 0 para qur eilimine todo 
            int fila = tabla.getSelectedRow();
            String ID = tabla.getValueAt(fila, 0).toString();
            
            try{
                String borrar = "DELETE FROM datos WHERE nombre = '" + ID + " '";
                PreparedStatement pst = cn.prepareStatement(borrar);
                pst.executeUpdate();
                mostrar("");
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,"algo salio mal en el registro "+e);
                
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

