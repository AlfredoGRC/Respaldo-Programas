        // TODO add your handling code here:
        
        
        String nom, eda;
        String SQL = "ejemplo";
        
        nom = nombre.getText();
        eda = edad.getText();
        
        SQL = "INSERT INTO datos (nombre, edad) VALUES (?, ?)";
        
        try{
             PreparedStatement pst =  cn.prepareStatement(SQL);//prepara las sentencias SQL pst = nombre de variable SQL es la variable de arriba
            pst.setString(1,nom);  // nombre de la variable donde gurdamos el TextBox
            pst.setString(2,eda); // nombre de la variable donde gurdamos el TextBox
  
            int n = pst.executeUpdate();
                            //actualizar --- consulta//
            mostrar(""); 
 
            if (n>=0){//array 
                JOptionPane.showMessageDialog(null,"Datos guardados ");  
                bloquear(); 
                limpiar();
            }
            
        }
        catch(SQLException e){
            
            Logger.getLogger(e_db.class.getName()).log(Level.SEVERE,null,e);
            
        }
