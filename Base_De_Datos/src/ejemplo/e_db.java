    void mostrar(String valor){
        
        DefaultTableModel modelo = new DefaultTableModel();//crear una tabla por defecto para mandar a llamar 
        
        modelo.addColumn("nombre");
        modelo.addColumn("edad");
        
        String SQL = "SELECT * FROM datos";
        String SQL1 = "SELECT * FROM datos WHERE nombre = ' "+ valor+" '";
        
        String res = (valor.equals("")) ? SQL : SQL1;
        
        String [] datos = new String[2];
        
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(res);
            
            while(rs.next()){
                datos[0] =  rs.getString(1);
                datos[1] = rs.getString(2);
                
                
                modelo.addRow(datos);
            }
            tabla.setModel(modelo);
        }
        catch(SQLException e){
            
            Logger.getLogger(e_db.class.getName()).log(Level.SEVERE, null,e);   
        }      
    }
